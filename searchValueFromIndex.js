let arr = [10, 33, 47, 52, 97, 46, 29, 81];
    var searchIndex = 3;

    function searchValueFromIndex(){
        for(let i=0; i<arr.length; i++){
            if(i === searchIndex){
                return arr[i];
            }
        }
    }

    var value = searchValueFromIndex();
